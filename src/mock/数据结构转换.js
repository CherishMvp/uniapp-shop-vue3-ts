function recursiveTransform(arr, prefix = '') {
  const result = {};

  arr.forEach((obj) => {
    const id = obj.id;
    const subcategoryId = obj.subcategory_id;

    if (!result[id]) {
      result[id] = {
        id: obj.id,
        name: obj.name,
        picture: obj.picture,
        imageBanners: obj.imageBanners,
        subcategory: {},
      };
    }

    if (!result[id].subcategory[subcategoryId]) {
      result[id].subcategory[subcategoryId] = {
        subcategory_id: obj.subcategory_id,
        subcategory_name: obj.subcategory_name,
        subcategory_picture: obj.subcategory_picture,
        [`goods`]: [],
      };
    }

    const goods = {
      [`${prefix}_id`]: obj[`${prefix}_id`],
      [`${prefix}_name`]: obj[`${prefix}_name`],
      [`${prefix}_desc`]: obj[`${prefix}_desc`],
      [`${prefix}_price`]: obj[`${prefix}_price`],
      [`${prefix}_picture`]: obj[`${prefix}_picture`],
      [`${prefix}_orderNum`]: obj[`${prefix}_orderNum`],
    };

    result[id].subcategory[subcategoryId][`goods`].push(goods);
  });

  const transformedData = [];

  for (const id in result) {
    const item = result[id];
    const subcategories = [];

    for (const subcategoryId in item.subcategory) {
      const subcategory = item.subcategory[subcategoryId];
      subcategories.push(subcategory);
    }

    item.subcategory = subcategories;
    transformedData.push(item);
  }

  return transformedData;
}

